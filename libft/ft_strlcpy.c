#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	src_n;
	size_t	i;

	src_n = 0;
	while (src[src_n])
		src_n++;
	if (dstsize == 0)
		return (src_n);
	i = 0;
	while (src[i] && i < (dstsize - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (src_n);
}
