#include "libft.h"

long	ft_atol(const char *nptr)
{
	long	sum;
	int		i;
	int		sign;

	sum = 0;
	i = 0;
	sign = 1;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
		{
			sign *= -1;
		}
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
		sum = (sum * 10) + (nptr[i++] - '0');
	return (sign * sum);
}
